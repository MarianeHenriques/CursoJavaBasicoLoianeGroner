package curso.java.aula11;

public class VariaveisInteira {

    public static void main(String[] arg){
        //Valor inteiro que ocupará pouco espaço na memoria
        byte idade01 = 20;

        //Valor inteiro um pouco maior
        short idade02 = 21;

        //Valor inteiro maior mais utilizado
        int idade03 = 22;

        //Valor inteiro que ocupa mais espaço na memoria e mais usado
        long idade04 = 23;

        System.out.println("Valor da variavel 01: "+ idade01);


    }
}
