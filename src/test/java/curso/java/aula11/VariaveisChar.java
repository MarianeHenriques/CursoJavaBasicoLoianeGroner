package curso.java.aula11;

public class VariaveisChar {

    public static void main(String[] args){

        //Essa representação é pouco usual apenas para conhecer
        char o = 'o';

        //Para usar esse metodo aspas simples
        char i = 'i';

        //Para representar em forma numerica
        System.out.println(o+i);

        //Para representar como junção de letras
        System.out.println(""+o+i);

    }
}
